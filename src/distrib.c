#include "lem_in.h"

void	distrib_ants(t_map *p)
{
	int ia[2];

	ia[1] = p->ants;
	while (ia[1] > 0)
	{
		ia[0] = -1;
		while (++ia[0] < p->k_w - 1)
		{
			if (ia[0] == p->k_w - 1 - 1)
				if ((p->w_a[ia[0]][0] + p->w_a[ia[0]][2] + 1) >=
				(p->w_a[ia[0] + 1][0] + p->w_a[ia[0] + 1][2] + 1))
				{
					p->w_a[ia[0] + 1][2] = p->w_a[ia[0] + 1][2] + 1;
					ia[1]--;
					break ;
				}
			if ((p->w_a[ia[0]][0] + p->w_a[ia[0]][2] + 1) <
			(p->w_a[ia[0] + 1][0] + p->w_a[ia[0] + 1][2] + 1))
			{
				p->w_a[ia[0]][2] = p->w_a[ia[0]][2] + 1;
				ia[1]--;
				break ;
			}
		}
	}
}

int		mins(int **w, int n)
{
	int i;
	int m;

	i = 0;
	m = 10000000;
	while (i < n)
	{
		if (w[i][0] + w[i][2] < m)
			m = w[i][0] + w[i][2];
		i++;
	}
	return (m);
}
